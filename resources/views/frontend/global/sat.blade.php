<h4>SAT Subject Test Percentiles</h4>
<p>As the term suggests, SAT Subject Test Percentile ranks compare a student's scores to other students who appeared for the same test. So for example, if a test takers SAT Math 2 Subject Test score is 500 and if the percentile rank for 500 is 50, then this basically means that the candidate has done better than 50 percent of the students who took the particular test, i.e. SAT Math 2 Subject Test.</p>
<p>Since a different percentage of students take the different subject exams under SAT Subject Tests, hence it would be technically incorrect to compare the SAT Subject Test Chemistry Percentile with SAT Subject Test Math 2 Percentile or vice versa. Candidates looking to see how they have fared in the SAT Subject Test with other candidates can check out SAT Subject Test Percentiles 2019 and for other past years on the official CollegeBoard website – the conducting body of the SAT exam.</p>
