<h4>How to check the SAT score?</h4>
<p>Candidates can access the SAT score report on their College Board profile.</p>
<p>Steps to check SAT scores</p>
<ul>
<li>Step 1- Visit the College Board's site</li>
<li>Step 2- Sign in using your ID and password</li>
<li>Step 3- Click on My Organiser and go to SAT scores</li>
<li>Step 4- Click on Access My Scores and enter your username and password</li>
<li>Step 5- Go to My Test Scores</li>
<li>Step 6- Your SAT scores will appear on the screen</li>
</ul>
<p>Candidates can send their score reports to the colleges using Score Choice.</p>
<h4>How to Get SAT and Subject Test Scores</h4>
<p>Once you are finished with the SAT exam, it is time to send exam scores to the colleges. This is one of the most crucial parts of the whole application process. Most colleges prefer to get the score reports directly from the College Board and not the copies of the report or score report labels on the transcripts.</p>
<h3><strong>When to Expect Scores</strong></h3>
<p>Your SAT score result will be available online around 4-5 weeks after your test date. Once the score result is available, the College Board begins sending SAT score reports to the colleges the candidate has selected, if any, at the time of registration.</p>
<h3><strong>Other Ways to Get Scores</strong></h3>
<p>Students who don't have easy access to the Internet can check out alternative methods of receiving their SAT Score, these include</p>
<ul>
<li>Paper Score Reports: Candidates who register by mail and don't have an active College Board online accounts will receive paper score reports.</li>
<li>Scores by phone: Candidates can also receive their scores over the phone. However, they would be required to pay an extra fee for the same.</li>
<li>Candidates can also access their scores by logging into their online score report and taking a print out of the same.&nbsp;</li>
</ul>
<h3><strong>What is Score Choice</strong></h3>
<p>With Score Choice, one gets the freedom to report the score he/she chooses to send to colleges. The candidate can choose the score report by test date or by individual test for SAT Subject Tests. However, before sending the scores always make sure to check with the college or the scholarship program where you are applying, as many colleges and scholarship programs require students to send all their scores. If the candidate doesn't select Score Choice then College Board will send all the available scores of the candidate to the colleges which he/she has selected as score recipients.</p>
<h3><strong>How to send Scores during Registration?</strong></h3>
<p>Applicants receive four free score reports while&nbsp;<a href="#">registering for the SAT exam</a>. &nbsp;Applicants get the option of sending these reports to their choice of colleges or scholarship programs for free. Aspirants can send the scores up to nine days of taking the test. After nine days they will be charged for sending the score reports. With this, the aspirant can send four reports for free. However, there is a catch, the aspirants are sending the scores without knowing their actual scores. This means, the candidates won't be able to choose the score they want to send or they won't be able to send their best scores. In this case, it is advisable to send the score reports to the colleges that want to see all the&nbsp;
<a href="#">SAT</a>&nbsp;scores of the candidates.</p>
