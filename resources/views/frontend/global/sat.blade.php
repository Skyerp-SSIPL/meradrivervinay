<h4>SAT Score</h4>
<p>SAT score reports consist of the following information</p>
<ul>
<li>The Total Score: As the title suggests, the Total Score is the sum of the two Section Scores and this score ranges from 400 to 1600.</li>
<li>Sectional Score: The sectional score ranges from 200-800 and is based on the performance of the candidate on the Math Section and Evidence-Based Reading &amp; Writing Sections.</li>
<li>Subscore (marks scored in each type of question): Subscores are multiple points for Reading, Writing and Language, and Math Sections, all ranging from 1 to 15. The main purpose of Sub scores is to provide additional insight into the students' performance.</li>
<li>Essay score (if applicable): Candidates who have undertaken the SAT with the essay will have the scores for Essay Reading (2-8 points), Essay Writing (2-8 points) and Essay Analysis (2-8 points) displayed under the Essay Details tab.</li>
<li>Student's percentile: The student's percentile score tells you how well the student has performed his SAT exam as compared to other test takers.&nbsp;</li>
</ul>
