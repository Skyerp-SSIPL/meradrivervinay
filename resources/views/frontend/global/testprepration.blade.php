                            <h5>TOEFL (Test of English as a Foreign Language)</h5>
                            <ul>
                                <li><strong>Overview:</strong> Measures the ability to understand and use English at the university level.</li>
                                <li> <strong>Format:</strong> Includes reading, writing, listening, and speaking sections.</li>
                                <li><strong>Acceptance:</strong> Accepted by over 11,000 institutions in more than 150 countries, including the US and Canada.</li>
                            </ul>

                            <h4>IELTS (International English Language Testing System)</h4>
                            <ul>
                                <li>Overview: Assesses listening, reading, writing, and speaking skills.</li>
                                <li>Format: Offered in two versions—Academic (for higher education) and General Training (for work or immigration).</li>
                                <li>Acceptance: Widely recognized in the UK, Australia, and Canada, and increasingly in the US.</li>
                            </ul>





                            <h4>PTE (Pearson Test of English)</h4>
                            <ul>
                                <li>Overview: Focuses on real-life English language skills.</li>
                                <li>Format: Computer-based test with speaking, writing, reading, and listening components.</li>
                                <li>Acceptance: Gaining traction among universities in Australia, the UK, and the US.
